# Perform arithmetic operations using the integer package

$x = 7.83;
$y = 5.25;

print("\$x = ", $x, "\n");
print("\$y = ", $y, "\n\n");

print("Floating point:\n\n");
print("\$x * \$y = ", $x*$y, "\n");
print("\$x / \$y = ", $x/$y, "\n");
print("\$x + \$y = ", $x+$y, "\n");
print("\$x - \$y = ", $x-$y, "\n");
print("\$x % \$y = ", $x%$y, "\n\n");

use integer;

print("Integer:\n\n");
